#include "libft.h"

char	*ft_strtrim(char const *s)
{
	int i;
	int j;

	i = 0;
	if (!*s || s[i] == '\0')
		return ("");
	while (s[i] == ' ' || s[i] == '\n' || s[i] == '\t')
	{
		i++;
		if (!s[i])
			return ("");
	}
	j = i;
	while (s[j])
	{
		j++;
	}
	j--;
	while (s[j] == ' ' || s[j] == '\n' || s[j] == '\t')
	{
		j--;
	}
	return (ft_strsub(s, i, (j - i + 1)));
}
