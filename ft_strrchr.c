#include <string.h>

char	*ft_strrchr(const char *s, int c)
{
	int i;
	char *r;

	i = 0;
	r = (char *)s;
	while (r[i])
		i++;
	while (r[i])
	{
		if (r[i] == c)
		{
			return (&r[i]);
		}
		i--;
	}
	return (NULL);
}
