void	ft_putnbr(int n)
{
	int copy;
	int mult;
	int j;

	copy = n;
	mult = 1;
	while(copy > 9)
	{
		mult *= 10;
		copy /= 10;
	}
	while(n > 0)
	{
		j = n / mult + 48;
		write(1, &j, 1);
		n -= n / mult * mult;
		mult /= 10;
	}
}
